    public function SaveOrder()
    {
        $device = new Order();

        $device->billing_email = request('email');
        $device->billing_name = request('name');
        $device->billing_city = request('city');
        $device->billing_province = request('province');
        $device->billing_phone = request('phone');
        $device->user_id  = '1';

        $device->save();

        return redirect('/');
    }
